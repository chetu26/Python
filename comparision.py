num1=10.5
num2=10

# greater than
print(num1>num2)

# less than
print(num1<num2)

# greater than and equal To
print(num1>=num2)

# less than and equal 
print(num1<=num2)

# equal 
print(num1==num2)

# not equal
print(num1!=num2)

# equal(identity)
print(num1 is num2)

# not equal (identity)
print(num1 is not num2)